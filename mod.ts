/**
 * @module
 *
 * This module exports a function to divide two numbers.
 *
 * ```ts
 * import { division } from "@flacial/division"
 *
 * console.log(division(10, 2)) // => 5
 * ```
 */

/**
 * Division two numbers in an easy way.
 *
 * @param a The first number
 * @param b The second number
 */
export function division(a: number, b: number): number {
  return a / b
}
